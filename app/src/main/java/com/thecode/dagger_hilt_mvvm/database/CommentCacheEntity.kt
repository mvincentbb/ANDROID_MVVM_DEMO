package com.thecode.dagger_hilt_mvvm.database

import androidx.room.Entity
import androidx.room.PrimaryKey

@Entity(tableName = "comments")
data class CommentCacheEntity (
    @PrimaryKey(autoGenerate = false)
    val id: Int,
    val postId: Int,
    val name: String,
    val email : String,
    val body: String
)

